public class Point
{
    private int xPunkt;
    private int yPunkt;

    public Point(int x, int y){
        this.xPunkt = x;
        this.yPunkt = y;

    }

    // Getter Implimentiert
    public int getXPunkt(){
        return xPunkt;
    }
    public int getYPunkt(){
        return yPunkt;
    }






}
